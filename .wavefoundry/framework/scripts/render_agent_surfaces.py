def _review_role_slug(carrier: ReviewProtocolCarrier) -> str | None:
    path = Path(carrier.destination)
    if path.suffix != ".md" or tuple(path.parts[:2]) != ("docs", "agents"):
        return None
    if "journals" in path.parts or "memory" in path.parts or "personas" in path.parts:
        return None
    return path.stem


def review_protocol_carriers(repo_root: Path | None = None) -> tuple[ReviewProtocolCarrier, ...]:
    """Return canonical carriers plus explicitly enabled native role wrappers.

    Native wrappers are enabled by their on-disk presence and are derived only
    from a registered canonical role.  An arbitrary wrapper therefore cannot
    opt itself into framework ownership merely by matching a directory glob.
    """

    carriers = list(REVIEW_PROTOCOL_CARRIER_REGISTRY)
    if repo_root is None:
        return tuple(carriers)
    seen = {carrier.destination for carrier in carriers}
    for carrier in REVIEW_PROTOCOL_CARRIER_REGISTRY:
        role = _review_role_slug(carrier)
        if role is None:
            continue
        candidates = (
            f".claude/agents/{role}.md",
            f".codex/skills/agent-role-{role}/SKILL.md",
        ) + ((".codex/skills/auto-guru/SKILL.md",) if role == "guru" else ())
        for destination in candidates:
            if destination in seen or not (repo_root / destination).is_file():
                continue
            carriers.append(
                ReviewProtocolCarrier(carrier.source_seed, destination, conditional_existing=True)
            )
            seen.add(destination)
    return tuple(carriers)


def review_protocol_carrier_manifest(repo_root: Path | None = None) -> tuple[str, ...]:
    return tuple(carrier.destination for carrier in review_protocol_carriers(repo_root))

