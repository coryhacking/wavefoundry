CODEX_MCP_CONFIG_TOML = dedent(
    """\
    [mcp_servers.wavefoundry]
    command = "python3"
    args = [".wavefoundry/framework/scripts/server.py"]
    """
)

# Wave 1p9pe (renderer-overwrite-safety): `.codex/config.toml` is an
# operator-extensible surface (approval modes, tool policy, other MCP servers).
# The renderer owns ONLY the marked region below; everything outside it is
# operator-owned and must survive every render byte-for-byte. TOML `#` comment
# markers mirror the markdown MARKER_BEGIN/MARKER_END precedent.
CODEX_CONFIG_MARKER_BEGIN = (
    "# waveframework:codex-mcp begin — generated by render_agent_surfaces.py; "
    "do not edit inside this region; add operator config outside it"
)
CODEX_CONFIG_MARKER_END = "# waveframework:codex-mcp end"

_CODEX_FRAMEWORK_KEYS = ("command", "args")

# Review-fix (1p9pe follow-up hardening): line-anchored marker-region match.
# Both markers must sit on their own lines (they are TOML comments) — a marker
# string embedded inside an operator string value must never select the
# replace-region branch, or the substitution would slice through the string.
# The line tails tolerate a trailing \r so a CRLF-rewritten config (editor or
# tooling re-encoded the file) still matches its marker region and gets a
# fresh framework block instead of perpetually fail-safing with a stale one.
_CODEX_MARKER_REGION_RE = re.compile(
    "^"
    + re.escape(CODEX_CONFIG_MARKER_BEGIN)
    + r"[ \t\r]*\n.*?^"
    + re.escape(CODEX_CONFIG_MARKER_END)
    + r"[ \t\r]*(?:\n|\Z)",
    re.MULTILINE | re.DOTALL,
)

# Review-fix: the unmarked-table migration must accept the TOML-equivalent
# header spellings — quoted keys ([mcp_servers."wavefoundry"]) and whitespace
# around the dot / inside the brackets — so those shapes MIGRATE instead of
# being re-declared by the append branch (a duplicate table is invalid TOML).
_CODEX_TABLE_HEADER_RE = re.compile(
    r"""^\[\s*(?:mcp_servers|"mcp_servers"|'mcp_servers')\s*\.\s*"""
    r"""(?:wavefoundry|"wavefoundry"|'wavefoundry')\s*\]\s*$"""
)

# Review-fix: hard bound on multi-line value absorption. The framework template
# has only ever emitted one-line values; a continuation run longer than this is
# a bracket-count desync (e.g. an unbalanced "[" inside an operator-edited
# string), and absorbing further would eat operator content.
_CODEX_ABSORB_MAX_VALUE_LINES = 10


def _codex_value_bracket_delta(fragment: str) -> int:
    """Net bracket-nesting delta for a TOML value fragment.

    Used only to absorb multi-line ``command``/``args`` values during the
    one-time unmarked-table migration. Plain counting cannot see string
    boundaries, so an unbalanced bracket INSIDE a string desyncs the count —
    ``upsert_codex_mcp_config`` guards that with the continuation-line cap
    and the end-of-absorption ``value_depth != 0`` fail-safe.
    """
    return (
        fragment.count("[")
        + fragment.count("{")
        - fragment.count("]")
        - fragment.count("}")
    )


def _codex_marker_in_multiline_string(value) -> bool:
    """True if any string in a parsed TOML value tree embeds a marker line.

    A MULTILINE operator string (triple-quoted) whose body reproduces a marker
    comment as a whole physical line defeats the line-anchored region regex:
    the region replace would slice through the string, and the sliced result
    can still parse, so a parse check alone is blind to the loss. Legitimate
    marked regions keep marker text only in TOML comment lines — which the
    parser drops — so marker text inside a PARSED string value that spans
    lines is always an embedding and no rewrite is safe. Single-line strings
    cannot line-anchor the regex (their opening quote precedes the marker on
    the physical line) and are tolerated.
    """
    if isinstance(value, str):
        return "\n" in value and (
            CODEX_CONFIG_MARKER_BEGIN in value or CODEX_CONFIG_MARKER_END in value
        )
    if isinstance(value, dict):
        return any(_codex_marker_in_multiline_string(item) for item in value.values())
    if isinstance(value, list):
        return any(_codex_marker_in_multiline_string(item) for item in value)
    return False


def _codex_normalized_doc(doc: dict) -> dict:
    """Normalize a parsed config for the semantic-equivalence exit guard.

    Strips exactly the differences a legitimate upsert makes — the framework-
    owned ``command``/``args`` keys of ``[mcp_servers.wavefoundry]`` (refreshed
    to the current template) and the creation of that table (or its
    ``mcp_servers`` parent) where it was absent. Marker comments are invisible
    to the parser and need no tolerance. After normalization, ANY difference
    between the existing and candidate documents is operator-owned content the
    merge would corrupt — valid-TOML content loss a parse check cannot flag.
    """
    normalized = copy.deepcopy(doc)
    servers = normalized.get("mcp_servers")
    if isinstance(servers, dict):
        table = servers.get("wavefoundry")
        if isinstance(table, dict):
            table.pop("command", None)
            table.pop("args", None)
            if not table:
                del servers["wavefoundry"]
        if not servers:
            del normalized["mcp_servers"]
    return normalized


def upsert_codex_mcp_config(
    existing: str | None,
    on_fail_safe=None,
) -> str:
    """Return new `.codex/config.toml` content with the framework region upserted.

    Non-destructive by contract (AGENTS.md: "Never overwrite project-local
    customizations"):

    - No file / empty file: return just the marked framework region.
    - Marker region present (both markers on their own lines; CRLF line
      endings tolerated): replace only the region between them; all operator
      content outside survives byte-for-byte, and a re-render is idempotent.
    - Markers absent but an unmarked top-level ``[mcp_servers.wavefoundry]``
      table exists (pre-marker renders; TOML-equivalent quoted/whitespace
      header spellings included): one-time migration — absorb the contiguous
      framework-owned ``command``/``args`` lines into the marked region
      without duplicating the table. Because the markers are TOML comments,
      any operator-added keys left in place directly after the end marker
      still belong to ``[mcp_servers.wavefoundry]``.
    - Neither markers nor table: append the marked region after the operator
      content.

    Single-exit fail-safe (review hardening): every branch that MERGES with
    operator content computes a candidate that is validated before being
    returned — it must parse with ``tomllib`` AND be semantically equivalent
    to the parsed existing content after normalizing the framework-owned
    differences (see ``_codex_normalized_doc``). If validation fails — or the
    merge looks ambiguous up front (marker text outside a well-formed region
    or embedded in a multiline string value, bracket-depth desync, oversized
    absorption) — the existing content is returned unchanged (a stale
    framework block is recoverable; a corrupted operator config is not). When
    that happens the optional ``on_fail_safe(reason)`` callback is invoked so
    the caller can warn loudly and skip reporting the file as written. One
    deliberate exception to the validated exit: the no-existing-content branch
    returns the constant framework block directly — it is renderer-constant
    content with no operator merge, so there is nothing to corrupt.
    """

    def fail_safe(reason: str) -> str:
        if on_fail_safe is not None:
            on_fail_safe(reason)
        return existing if existing is not None else ""

    import tomllib

    block = (
        f"{CODEX_CONFIG_MARKER_BEGIN}\n"
        f"{CODEX_MCP_CONFIG_TOML.rstrip()}\n"
        f"{CODEX_CONFIG_MARKER_END}\n"
    )
    if existing is None or existing.strip() == "":
        # Unvalidated by design: constant framework content, no operator merge.
        return block

    # Parse the existing content once, up front. An unparseable operator file
    # (already broken before we arrived) keeps the historical branch behavior:
    # the string-embedding pre-check and the semantic-equivalence exit guard
    # below both require a parsed baseline and are skipped.
    existing_doc: dict | None = None
    try:
        existing_doc = tomllib.loads(existing)
    except Exception:
        existing_doc = None

    # Fail-safe BEFORE branch selection: a multiline operator string that
    # reproduces a marker comment as a whole physical line would match the
    # line-anchored region regex and slice the string — and the sliced result
    # can still parse, so only this structural check catches it early with a
    # precise reason (the equivalence exit guard is the backstop).
    if existing_doc is not None and _codex_marker_in_multiline_string(existing_doc):
        return fail_safe(
            "marker text found inside a multiline operator string value; "
            "region matching would slice the string — cannot upsert safely"
        )

    candidate: str
    region_match = _CODEX_MARKER_REGION_RE.search(existing)
    if region_match is not None:
        candidate = existing[: region_match.start()] + block + existing[region_match.end():]
    elif CODEX_CONFIG_MARKER_BEGIN in existing or CODEX_CONFIG_MARKER_END in existing:
        # Marker text exists but not as a well-formed whole-line region (e.g.
        # inside an operator string, or a half-deleted region). Any rewrite
        # here risks slicing operator content — leave the file alone.
        return fail_safe(
            "marker text present outside a well-formed marker region; "
            "cannot upsert safely"
        )
    else:
        lines = existing.splitlines(keepends=True)
        header_index = next(
            (
                index
                for index, line in enumerate(lines)
                if _CODEX_TABLE_HEADER_RE.match(line.strip())
            ),
            None,
        )
        if header_index is None:
            candidate = existing.rstrip("\n") + "\n\n" + block
        else:
            # One-time absorption of the unmarked framework table: consume the
            # header plus the contiguous run of framework-owned assignments
            # (with multi-line value continuations). Stop at the first
            # operator-authored line (comment, unknown key, blank-line-then-
            # next-table) so operator content — including subtables like
            # [mcp_servers.wavefoundry.tools.*] — stays in place.
            absorbed_end = header_index + 1
            index = header_index + 1
            value_depth = 0
            continuation_lines = 0
            while index < len(lines):
                stripped = lines[index].strip()
                if value_depth > 0:
                    continuation_lines += 1
                    if continuation_lines > _CODEX_ABSORB_MAX_VALUE_LINES:
                        return fail_safe(
                            "unmarked-table migration would absorb more than "
                            f"{_CODEX_ABSORB_MAX_VALUE_LINES} value-continuation "
                            "lines — likely a bracket-count desync"
                        )
                    value_depth += _codex_value_bracket_delta(stripped)
                    index += 1
                    absorbed_end = index
                    continue
                if stripped == "":
                    # Tentatively skip: absorbed only if more framework lines follow.
                    index += 1
                    continue
                key, separator, value = stripped.partition("=")
                if separator == "=" and key.strip().strip("\"'") in _CODEX_FRAMEWORK_KEYS:
                    value_depth = _codex_value_bracket_delta(value)
                    continuation_lines = 0
                    index += 1
                    absorbed_end = index
                    continue
                break
            if value_depth != 0:
                # Bracket depth never returned to zero: an unbalanced bracket
                # (typically inside an operator-modified string) desynced the
                # counter and absorption would silently eat operator content
                # — possibly producing VALID TOML the parse guard below cannot
                # flag. Fail safe instead.
                return fail_safe(
                    "unmarked-table migration ended with unbalanced bracket "
                    "depth — refusing to absorb (operator content would be lost)"
                )
            candidate = "".join(lines[:header_index] + [block] + lines[absorbed_end:])

    # Single exit: no merged content leaves this function unvalidated.
    try:
        candidate_doc = tomllib.loads(candidate)
    except Exception:
        # Never write a config Codex cannot parse. Leaving the file
        # untouched keeps the operator's working config; the framework
        # block simply stays stale until resolved manually.
        return fail_safe("merged result would not parse as TOML")
    # Semantic-equivalence guard: the parse check alone is blind to
    # valid-TOML content loss (an absorption desync or string slice can
    # eat operator content and still produce parseable TOML). After
    # normalizing the framework-owned differences, existing and candidate
    # must describe the SAME document; anything else is operator content
    # the merge would silently drop or alter.
    if existing_doc is not None and (
        _codex_normalized_doc(existing_doc) != _codex_normalized_doc(candidate_doc)
    ):
        return fail_safe(
            "merged result would change operator-owned parsed content "
            "(valid-TOML content loss the parse check cannot flag) — "
            "refusing to write"
        )
    return candidate


def guru_available(repo_root: Path) -> bool:
    return (repo_root / GURU_ROLE_REL).is_file()


def upsert_marked_region(text: str, region: str) -> str:
    block = f"{MARKER_BEGIN}\n{region.rstrip()}\n{MARKER_END}"
    pattern = re.compile(
        re.escape(MARKER_BEGIN) + r".*?" + re.escape(MARKER_END),
        re.DOTALL,
    )
    if pattern.search(text):
        return pattern.sub(block, text, count=1)
    return text.rstrip() + "\n\n" + block + "\n"


def insert_after_heading(text: str, heading: str, inner: str) -> str:
    """Insert marked block after the first line matching ``heading`` (markdown ## title)."""
    if MARKER_BEGIN in text:
        return upsert_marked_region(text, inner)
    lines = text.splitlines(keepends=True)
    for index, line in enumerate(lines):
        if line.strip() == heading:
            insert_at = index + 1
            while insert_at < len(lines) and lines[insert_at].strip() == "":
                insert_at += 1
            marked = f"{MARKER_BEGIN}\n{inner}\n{MARKER_END}"
            new_lines = lines[:insert_at] + ["\n", marked, "\n"] + lines[insert_at:]
            return "".join(new_lines)
    marked = f"{MARKER_BEGIN}\n{inner}\n{MARKER_END}"
    return text.rstrip() + "\n\n" + marked + "\n"


def strip_legacy_auto_guru_section(text: str) -> str:
    """Remove unmarked ``## Code and documentation questions (auto-Guru)`` sections."""
    pattern = re.compile(
        r"\n## Code and documentation questions \(auto-Guru\)\n.*?(?=\n## |\n<!-- waveframework:|\Z)",
        re.DOTALL,
    )
    return pattern.sub("\n", text)


def strip_legacy_auto_guru_lines(text: str) -> str:
    """Remove hand-seeded auto-Guru bullets superseded by marker blocks."""
    pattern = re.compile(
        r"^\- Code and documentation Q&A:.*\n",
        re.MULTILINE,
    )
    if MARKER_BEGIN in text:
        parts = []
        in_marker = False
        for line in text.splitlines(keepends=True):
            if MARKER_BEGIN in line:
                in_marker = True
                parts.append(line)
                continue
            if MARKER_END in line:
                in_marker = False
                parts.append(line)
                continue
            if in_marker:
                parts.append(line)
                continue
            if line.strip().startswith("- Code and documentation Q&A:"):
                continue
            parts.append(line)
        return "".join(parts)
    return pattern.sub("", text)


def patch_thin_pointer(
    path: Path,
    inner: str,
    *,
    after_heading: str | None = None,
) -> bool:
    if not path.is_file():
        return False
    original = strip_legacy_auto_guru_section(
        strip_legacy_auto_guru_lines(path.read_text(encoding="utf-8"))
    )
    if after_heading:
        updated = insert_after_heading(original, after_heading, inner)
    else:
        updated = upsert_marked_region(original, inner)
    if updated != original:
        path.write_text(updated, encoding="utf-8")
        return True
    return False


def patch_root_bridge(path: Path) -> bool:
    """Ensure the root CLAUDE.md is a real ``@AGENTS.md`` import (1p5xc).

    Replaces the legacy prose pointer ("Thin pointer. Read `AGENTS.md` first…")
    with a marked ``@AGENTS.md`` import block so Claude loads AGENTS.md
    deterministically. Idempotent. This is the only ``@import`` the framework
    adopts and only at the root — never per-folder.
    """
    if not path.is_file():
        return False
    original = path.read_text(encoding="utf-8")
    text = original

    # Refresh an existing bridge block in place.
    bridge_re = re.compile(
        re.escape(ROOT_BRIDGE_MARKER_BEGIN) + r".*?" + re.escape(ROOT_BRIDGE_MARKER_END),
        re.DOTALL,
    )
    if bridge_re.search(text):
        text = bridge_re.sub(ROOT_BRIDGE_BLOCK, text, count=1)
        if text != original:
            path.write_text(text, encoding="utf-8")
            return True
        return False

    # Replace the legacy prose pointer if present; otherwise insert after the H1.
    if _LEGACY_THIN_POINTER_RE.search(text):
        text = _LEGACY_THIN_POINTER_RE.sub(ROOT_BRIDGE_BLOCK + "\n", text, count=1)
    else:
        lines = text.splitlines(keepends=True)
        insert_at = 0
        for index, line in enumerate(lines):
            if line.startswith("# "):
                insert_at = index + 1
                while insert_at < len(lines) and lines[insert_at].strip() == "":
                    insert_at += 1
                break
        block = ROOT_BRIDGE_BLOCK + "\n"
        new_lines = lines[:insert_at] + ["\n", block, "\n"] + lines[insert_at:]
        text = "".join(new_lines)

    if text != original:
        path.write_text(text, encoding="utf-8")
        return True
    return False


def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    # newline="" disables newline translation so the embedded line terminators are written
    # VERBATIM, byte-identical on every rendering host (mirrors render_platform_surfaces.write_text,
    # wave 1p7tz). With the default newline=None, a re-render on native Windows translates every
    # "\n" → os.linesep ("\r\n"), so the freshly generated agent surfaces (auto-guru.mdc, guru.md,
    # SKILL.md, config.toml) would gain CRLF and diff full-file on re-render. The source strings
    # already carry LF terminators, so newline="" is right for all hosts.
    with path.open("w", encoding="utf-8", newline="") as handle:
        handle.write(content)


def _carrier_protocol_block(carrier: ReviewProtocolCarrier) -> str:
    """Return the shared minimum plus any destination-specific obligations."""

    block = REVIEW_PROTOCOL_CARRIER_BLOCK
    if carrier.source_seed in {
        "221-code-reviewer.prompt.md",
        "239-qa-reviewer.prompt.md",
    }:
        block += "\n\n" + INDEPENDENT_REFERENCE_CARRIER_BLOCK
    if carrier.destination != "docs/agents/qa-reviewer.md":
        return block
    return block + "\n\n" + dedent(
        """\
        ### QA evidence integrity

        Required QA evidence must run with zero unintended skips, reach the
        named public path, use realistic boundary values, make non-vacuous
        assertions, and demonstrate detection of the known-bad behavior. A
        skipped, vacuous, impossible-shape, or wrong-reason failure is not
        approval evidence.
        """
    ).strip()


def _upsert_review_protocol_region(text: str, block: str) -> str | None:
    """Return text with the owned protocol block refreshed, or ``None`` on malformed markers."""

    begin_count = text.count(REVIEW_PROTOCOL_MARKER_BEGIN)
    end_count = text.count(REVIEW_PROTOCOL_MARKER_END)
    if begin_count != end_count or begin_count > 1:
        return None
    marked = (
        f"{REVIEW_PROTOCOL_MARKER_BEGIN}\n"
        f"{block}\n"
        f"{REVIEW_PROTOCOL_MARKER_END}"
    )
    if begin_count == 1:
        region = re.compile(
            r"^" + re.escape(REVIEW_PROTOCOL_MARKER_BEGIN) + r"[ \t\r]*\n.*?^"
            + re.escape(REVIEW_PROTOCOL_MARKER_END) + r"[ \t\r]*(?=\n|\Z)",
            re.MULTILINE | re.DOTALL,
        )
        if not region.search(text):
            return None
        return region.sub(marked, text, count=1)
    # Project-authored bytes are an exact prefix on first adoption. Do not use
    # rstrip(): trailing spaces and blank lines outside the owned region belong
    # to the project even when they look cosmetically redundant.
    separator = "" if not text or text.endswith("\n") else "\n"
    return text + separator + "\n" + marked + "\n"


